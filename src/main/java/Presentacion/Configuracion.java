    private void jAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAgregarActionPerformed
        // Get the information from the menu and Create a new word
        String sqlSent;
        try{
            if(jID.getText().length()==0 && (jEspanol.getText().length()!=0) && jIngles.getText().length()!=0){
                this.pp.setEspanol(jEspanol.getText());
                this.pp.setIngles(jIngles.getText());
                this.pp.setCategoria(jCategoria.getText());
                this.pp.setImagen(jImagen.getText());
                sqlSent = "INSERT INTO ListaDePalabras(Espanol, Ingles, Categoria, Imagen)" 
                        + " VALUES('" + this.pp.getEspanol() + "', '" + this.pp.getIngles() + "', '" + this.pp.getCategoria() + "', '" 
                        + this.pp.getImagen() + "');"; 
                this.newDict.agregarPalabra(sqlSent);
                mostrarDatosTabla("");
                reiniciarEspacioInputs();
            }
            else{
                this.pp.setID(Integer.parseInt(jID.getText()));
                this.pp.setEspanol(jEspanol.getText());
                this.pp.setIngles(jIngles.getText());
                this.pp.setCategoria(jCategoria.getText());
                this.pp.setImagen(jImagen.getText());
                sqlSent = "INSERT INTO ListaDePalabras(ID, Espanol, Ingles, Categoria, Imagen)"
                        + " VALUES('" + this.pp.getID() + "', '" + this.pp.getEspanol() + "', '" + this.pp.getIngles() 
                        + "', '" + this.pp.getCategoria()+ "', '" + this.pp.getImagen() + "');";          
                this.newDict.agregarPalabra(sqlSent);
                mostrarDatosTabla("");
                reiniciarEspacioInputs();
            }
        }
        catch(NumberFormatException ex){
            System.out.println("Error: Todos los campos estan vacios (" + ex + ")");
        }
        actualizarEstadoBotones();
    }//GEN-LAST:event_jAgregarActionPerformed
 
