    private void mostrarDatosTabla(String sqlSent){
        List<ParPalabra> listaDePalabras = this.newDict.DictListaDePalabras(sqlSent);
        Object[][] datosFila = new Object[1][5];
        Object[] datosColumnas = {"ID", "Espanol", "Ingles", "Categoria", "Imagen"};
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(datosColumnas);
        this.jDictTable.setModel(model);
        
        for(ParPalabra item: listaDePalabras){
            datosFila[0][0] = item.getID();
            datosFila[0][1] = item.getEspanol();
            datosFila[0][2] = item.getIngles();
            datosFila[0][3] = item.getCategoria();
            datosFila[0][4] = item.getImagen();
            model.addRow(datosFila[0]);
        }
    }   
    
    public void actualizarEstadoBotones(){
        boolean vacioID = this.jID.getText().isEmpty();
        boolean vacioEspanol = this.jEspanol.getText().isEmpty();
        boolean vacioIngles = this.jIngles.getText().isEmpty();
        
        // Condicion para boton Agregar Palabra
        if(vacioEspanol || vacioIngles){
            this.jAgregar.setEnabled(false);
        }
        else{
            this.jAgregar.setEnabled(true);
        }
        
        // Condicion para botones Actualizar y Eliminar Palabra
        if(vacioEspanol || vacioIngles || vacioID){
            this.jActualizar.setEnabled(false);
            this.jEliminar.setEnabled(false);
        }
        else{
            this.jActualizar.setEnabled(true);
            this.jEliminar.setEnabled(true);
        }
    }

