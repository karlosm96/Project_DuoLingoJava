    private void jConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jConsultarActionPerformed
        // TODO add your handling code here:
        String sqlSent;
        try{
            if(this.jID.getText().isEmpty()){
                this.pp.setID(-1);
            }
            else{
                this.pp.setID(Integer.parseInt(this.jID.getText()));
            }
            this.pp.setEspanol(jEspanol.getText());
            this.pp.setIngles(jIngles.getText());
            this.pp.setCategoria(jCategoria.getText());
            this.pp.setImagen(jImagen.getText());

            if(this.pp.getID() == -1 && this.pp.getEspanol() == "NoEspanol" && this.pp.getIngles() == "NoIngles" 
                    && this.pp.getCategoria() == "NoCategoria" && this.pp.getImagen() == "NoImage"){
                System.out.println("Estamos aca");
                sqlSent = "";
            }
            else{
                sqlSent = " WHERE ID='" + this.pp.getID() + "' OR Espanol='" + this.pp.getEspanol() 
                    + "' OR Ingles='" + this.pp.getIngles() + "' OR Categoria='" + this.pp.getCategoria() + "';";              
            }
            mostrarDatosTabla(sqlSent);
            reiniciarEspacioInputs();
        }
        catch(NumberFormatException ex){
            System.out.println("Error: Input de ID invalido (" + ex + ")");
        }
        actualizarEstadoBotones();
    }//GEN-LAST:event_jConsultarActionPerformed

